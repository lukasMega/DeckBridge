// Regenerates ts/src/assets/font-atlas.ts from the Spleen bitmap font (BSD
// 2-Clause, https://github.com/fcambus/spleen). Run manually when changing
// fonts/glyph set: node scripts/gen-font-atlas.mjs
// Downloads the BDF sources from GitHub (they are not vendored).
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const SPLEEN_RAW = 'https://raw.githubusercontent.com/fcambus/spleen/master';
const FONTS = [
  { name: 'FONT_BIG', file: 'spleen-16x32.bdf' },
  { name: 'FONT_SMALL', file: 'spleen-8x16.bdf' },
];
// ASCII printable + degree sign (weather widget).
const CODEPOINTS = [...Array.from({ length: 95 }, (_, i) => 32 + i), 0xb0];

/** Parse a BDF into { w, h, glyphs: Map<codepoint, rows[][]> } where rows are
 *  cell-sized boolean arrays (top→bottom). */
function parseBdf(text) {
  const lines = text.split('\n');
  let fbb = null; // [w, h, xoff, yoff]
  const glyphs = new Map();
  let i = 0;
  while (i < lines.length) {
    const line = lines[i];
    if (line.startsWith('FONTBOUNDINGBOX ')) {
      fbb = line.split(/\s+/).slice(1, 5).map(Number);
    } else if (line.startsWith('STARTCHAR')) {
      let encoding = -1;
      let bbx = null;
      const bitmapHex = [];
      i++;
      for (; !lines[i].startsWith('ENDCHAR'); i++) {
        const l = lines[i];
        if (l.startsWith('ENCODING ')) encoding = Number(l.slice(9));
        else if (l.startsWith('BBX ')) bbx = l.split(/\s+/).slice(1, 5).map(Number);
        else if (l === 'BITMAP') {
          for (i++; !lines[i].startsWith('ENDCHAR'); i++) bitmapHex.push(lines[i].trim());
          break;
        }
      }
      if (encoding >= 0 && bbx) glyphs.set(encoding, { bbx, bitmapHex });
    }
    i++;
  }
  if (!fbb) throw new Error('no FONTBOUNDINGBOX');

  const [cellW, cellH, fx, fy] = fbb;
  const out = new Map();
  for (const [cp, { bbx, bitmapHex }] of glyphs) {
    const [bw, bh, bx, by] = bbx;
    const cell = Array.from({ length: cellH }, () => new Array(cellW).fill(false));
    const x0 = bx - fx;
    const y0 = cellH + fy - (bh + by); // top row of the glyph box within the cell
    for (let r = 0; r < bh; r++) {
      const rowBits = parseInt(bitmapHex[r], 16)
        .toString(2)
        .padStart(bitmapHex[r].length * 4, '0');
      for (let c = 0; c < bw; c++) {
        if (rowBits[c] === '1') {
          const y = y0 + r;
          const x = x0 + c;
          if (y >= 0 && y < cellH && x >= 0 && x < cellW) cell[y][x] = true;
        }
      }
    }
    out.set(cp, cell);
  }
  return { w: cellW, h: cellH, glyphs: out };
}

/** Pack the selected codepoints: per glyph ceil(w/8) bytes per row, MSB-first. */
function packFont({ w, h, glyphs }) {
  const rowBytes = Math.ceil(w / 8);
  const bytes = [];
  for (const cp of CODEPOINTS) {
    const cell = glyphs.get(cp);
    if (!cell) throw new Error(`missing glyph U+${cp.toString(16)}`);
    for (let y = 0; y < h; y++) {
      for (let b = 0; b < rowBytes; b++) {
        let v = 0;
        for (let bit = 0; bit < 8; bit++) {
          const x = b * 8 + bit;
          if (x < w && cell[y][x]) v |= 0x80 >> bit;
        }
        bytes.push(v);
      }
    }
  }
  return Buffer.from(bytes).toString('base64');
}

const parts = [];
let version = '';
for (const { name, file } of FONTS) {
  const res = await fetch(`${SPLEEN_RAW}/${file}`);
  if (!res.ok) throw new Error(`${file}: HTTP ${res.status}`);
  const text = await res.text();
  version ||= /Spleen \S+ ([\d.]+)/.exec(text)?.[1] ?? 'unknown';
  const font = parseBdf(text);
  parts.push(
    `export const ${name}: BitmapFont = {\n` +
      `  width: ${font.w},\n  height: ${font.h},\n` +
      `  bits:\n    '${packFont(font)}',\n};\n`,
  );
}

const header = `// AUTO-GENERATED by scripts/gen-font-atlas.mjs — DO NOT EDIT
// Glyphs from Spleen ${version} (c) 2018-2026 Frederic Cambus — BSD 2-Clause license.
// https://github.com/fcambus/spleen — license text: see LICENSE-spleen.txt (repo scripts/).
// Packed monochrome cells for codepoints 32..126 + 0xB0 (°), in that order:
// per glyph ceil(width/8) bytes per row (MSB-first), height rows top→bottom.

/** Index of a codepoint in the packed atlas, or -1 if not covered. */
export function fontGlyphIndex(codepoint: number): number {
  if (codepoint >= 32 && codepoint <= 126) return codepoint - 32;
  return codepoint === 0xb0 ? 95 : -1;
}

export interface BitmapFont {
  readonly width: number;
  readonly height: number;
  /** base64 of the packed glyph bitmaps. */
  readonly bits: string;
}

`;

const outPath = join(dirname(fileURLToPath(import.meta.url)), '../ts/src/assets/font-atlas.ts');
writeFileSync(outPath, header + parts.join('\n'));
console.log(`wrote ${outPath}`);
